    var hero2Speed = 0
    if let temp = superHero1.speed {
        hero1Speed = temp
    }
    if let temp2 = superHero2.speed {
        hero2Speed = temp2
    }

    if hero1Speed > hero2Speed {
        return "\(superHero1.name) рЃБрЃцрЃарЃЮ рЃАрЃгрЃарЃљрЃцрЃўрЃљ (\(hero1Speed)), рЃЋрЃўрЃЊрЃарЃћ \(superHero2.name) (\(hero2Speed))"
