        return "\(superHero2.name) рЃБрЃцрЃарЃЮ рЃАрЃгрЃарЃљрЃцрЃўрЃљ (\(hero2Speed)), рЃЋрЃўрЃЊрЃарЃћ \(superHero1.name) (\(hero1Speed))"
